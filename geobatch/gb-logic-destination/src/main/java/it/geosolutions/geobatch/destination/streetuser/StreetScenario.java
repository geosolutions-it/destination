package it.geosolutions.geobatch.destination.streetuser;


public class StreetScenario {

	private Integer idScenario;
	private String descrizioneScenario;
	private Double tempoDiCoda;
	
	public Integer getIdScenario() {
		return idScenario;
	}
	public void setIdScenario(Integer idScenario) {
		this.idScenario = idScenario;
	}
	public String getDescrizioneScenario() {
		return descrizioneScenario;
	}
	public void setDescrizioneScenario(String descrizioneScenario) {
		this.descrizioneScenario = descrizioneScenario;
	}
	public Double getTempoDiCoda() {
		return tempoDiCoda;
	}
	public void setTempoDiCoda(Double tempoDiCoda) {
		this.tempoDiCoda = tempoDiCoda;
	}	
	
	
}
